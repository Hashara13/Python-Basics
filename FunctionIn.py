#math functions
import math
print(abs(-45))
print(math.ceil(-45))
print(min(-45,100,54))
print(round(56.645,1))
print(math.sqrt(5))

#random numbers
import random
print(random.choice(range(45)))
random.seed()
print(random.random())

#Trigonamity Functions
print(math.sin(5))
print(math.sin(math.pi))
print(math.asin(0.5))
print(math.degrees(0))
print(math.radians(45))

